export const Lid = ({
  scaleX,
  scaleY,
  rotate,
  translate,
}: {
  scaleX: MotionValue<number>;
  scaleY: MotionValue<number>;
  rotate: MotionValue<number>;
  translate: MotionValue<number> | MotionValue<string>;
}) => {
  const { scrollYProgress } = useScroll();
  const scrollBase = 0.2;
  const scrollIncrement = 0.05;

  const gradientSize = useTransform(
    scrollYProgress,
    [scrollBase, scrollBase + 6 * scrollIncrement],
    [0, 300],
  );
  const screenOpacity = useTransform(
    scrollYProgress,
    [scrollBase - 2 * scrollIncrement, scrollBase + 4 * scrollIncrement],
    [0.2, 1],
  );
  const gradientRadius = useTransform(
    scrollYProgress,
    [
      scrollBase,
      scrollBase + 5 * scrollIncrement,
      scrollBase + 6 * scrollIncrement,
    ],
    [100, 60, 0],
  );
  const textOpacity = useTransform(
    scrollYProgress,
    [scrollBase - scrollIncrement, scrollBase + 1 * scrollIncrement],
    [0, 1],
  );

  const height = useMotionTemplate`${gradientSize}%`;
  const width = useMotionTemplate`calc(${gradientSize}% / 1.2)`;

  return (
    <div className="relative [perspective:800px]">
      <div
        style={{
          transform: "perspective(800px) rotateX(-25deg) translateZ(0px)",
          transformOrigin: "bottom",
          transformStyle: "preserve-3d",
        }}
        className="relative h-[12rem] w-full lg:w-[32rem] rounded-2xl bg-[#010101] p-2"
      ></div>
      <motion.div
        style={{
          scaleX: scaleX,
          scaleY: scaleY,
          rotateX: rotate,
          translateY: translate,
          transformStyle: "preserve-3d",
          transformOrigin: "top",
          opacity: screenOpacity,
        }}
        className={cn(
          "absolute inset-0 h-80 lg:h-96 w-full lg:w-[32rem] rounded-2xl bg-[#010101] backdrop-blur-lg border dark:border-neutral-800 drop-shadow-lg drop-shadow-emerald-700/40",
          "flex items-center justify-center overflow-hidden",
        )}
      >
        <motion.div
          style={{
            width,
            height,
          }}
        >
          <MotionAnimatedBackground
            containerClassName="w-full h-full"
            className="flex items-center justify-center text-center h-full w-full shadow-lg shadow-emerald-500"
            style={{ borderRadius: gradientRadius }}
            size="250%"
            {...gradientColours}
          >
            <motion.h1
              style={{ opacity: textOpacity }}
              className="font-bold text-transparent bg-clip-text bg-gradient-to-b from-neutral-50 to-neutral-100 text-nowrap m-10 text-3xl md:text-4xl p-4 z-10"
            >
              The best time to start
              <br />
              planning for the future?
              <br />
              <br />
              Right now
            </motion.h1>
          </MotionAnimatedBackground>
        </motion.div>
      </motion.div>
    </div>
  );
};

