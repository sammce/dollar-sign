const useHeight = () => {
  const [height, setHeight] = useState(1000);

  useEffect(() => {
    let timeout: NodeJS.Timeout | null = null;

    const handleResize = () => {
      if (timeout) {
        clearTimeout(timeout);
      }

      timeout = setTimeout(() => {
        setHeight(window.innerHeight);
      }, 300);
    };

    handleResize();
    window.addEventListener("resize", handleResize);

    return () => {
      window.removeEventListener("resize", handleResize);
      if (timeout) clearTimeout(timeout);
    };
  }, []);

  return height;
};

export const MacbookScroll = ({
  showGradient,
  title,
  badge,
}: {
  src?: string;
  showGradient?: boolean;
  title?: string | React.ReactNode;
  badge?: React.ReactNode;
}) => {
  const ref = useRef<HTMLDivElement>(null);
  const { scrollYProgress } = useScroll({
    target: ref,
    offset: ["start center", "end start"],
  });

  const isMobile = useIsMobile();
  const height = useHeight();

  const multiplier = isMobile ? 1.9 : 1.4;

  const scaleX = useTransform(
    scrollYProgress,
    [0, 0.3],
    [1.2, isMobile ? 1.5 : 1.5],
  );
  const scaleY = useTransform(
    scrollYProgress,
    [0, 0.3],
    [0.6, isMobile ? 1.5 : 1.5],
  );
  const translate = useTransform(
    scrollYProgress,
    [isMobile ? 0.1 : 0, 1],
    [0, height * multiplier],
  );

  const rotate = useTransform(
    scrollYProgress,
    [0.1, 0.12, 0.25],
    [-28, -28, 0],
  );
  const textTransform = useTransform(scrollYProgress, [0, 0.35], [0, 100]);
  const textOpacity = useTransform(scrollYProgress, [0.1, 0.3], [1, 0]);

  return (
    <div
      ref={ref}
      className="h-[180vh] lg:flex shrink-0 transform flex-col items-center justify-start [perspective:800px] pt-0 -mt-72 scale-[60%] md:scale-[100%] md:mt-0"
    >
      <motion.h1
        style={{
          translateY: textTransform,
          opacity: textOpacity,
        }}
        className="mb-10 text-center text-5xl font-bold text-neutral-800 dark:text-white"
      >
        {title || (
          <span>
            All your finances
            <br /> in one place
          </span>
        )}
      </motion.h1>
      {/* Lid */}
      <Lid
        scaleX={scaleX}
        scaleY={scaleY}
        rotate={rotate}
        translate={translate}
      />
      {/* Base area */}
      <div className="relative -z-10 h-[22rem] w-full md:w-[32rem] rounded-2xl bg-gray-200 dark:bg-[#272729]">
        {/* above keyboard bar */}
        <div className="relative h-10 w-full">
          <div className="absolute inset-x-0 mx-auto h-4 w-[80%] bg-[#050505]" />
        </div>
        <div className="relative flex">
          <div className="mx-auto h-full w-[10%] overflow-hidden">
            <SpeakerGrid />
          </div>
          <div className="mx-auto h-full w-[80%]">
            <Keypad />
          </div>
          <div className="mx-auto h-full w-[10%] overflow-hidden">
            <SpeakerGrid />
          </div>
        </div>
        <Trackpad />
        <div className="absolute inset-x-0 bottom-0 mx-auto h-2 w-20 rounded-tl-3xl rounded-tr-3xl bg-gradient-to-t from-[#272729] to-[#050505]" />
        {showGradient && (
          <div className="absolute inset-x-0 bottom-0 z-50 h-40 w-full bg-gradient-to-t from-white via-white to-transparent dark:from-black dark:via-black"></div>
        )}
        {badge && <div className="absolute bottom-4 left-4">{badge}</div>}
      </div>
    </div>
  );
};

